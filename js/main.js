function $(selector, scope = document) {
  return scope.querySelector(selector);
}

function $$(selector, scope = document) {
  return Array.from(scope.querySelectorAll(selector));
}

function setupHeader() {
  const header = $("#siteHeader");
  const menuToggle = $("#menuToggle");
  const mobileMenu = $("#mobileMenu");

  const onScroll = () => {
    if (!header) return;
    header.classList.toggle("scrolled", window.scrollY > 20);
  };

  onScroll();
  window.addEventListener("scroll", onScroll, { passive: true });

  if (menuToggle && mobileMenu) {
    menuToggle.addEventListener("click", () => {
      mobileMenu.classList.toggle("open");
      document.body.classList.toggle("menu-open", mobileMenu.classList.contains("open"));
    });

    $$("a", mobileMenu).forEach(link => {
      link.addEventListener("click", () => {
        mobileMenu.classList.remove("open");
        document.body.classList.remove("menu-open");
      });
    });
  }
}

function setupReveal() {
  const items = $$(".reveal");
  if (!items.length) return;

  if (!("IntersectionObserver" in window)) {
    items.forEach(item => item.classList.add("is-visible"));
    return;
  }

  const observer = new IntersectionObserver(entries => {
    entries.forEach(entry => {
      if (!entry.isIntersecting) return;
      entry.target.classList.add("is-visible");
    });
  }, {
    threshold: 0.14,
    rootMargin: "0px 0px -40px 0px"
  });

  items.forEach(item => observer.observe(item));
}

function setupIntroReveal() {
  const intro = $("#introReveal");
  if (!intro || intro.dataset.ready === "true") return;

  const words = intro.textContent.trim().split(/\s+/);
  intro.innerHTML = words.map(word => `<span>${word}</span>`).join(" ");
  intro.dataset.ready = "true";

  const spans = $$("span", intro);

  const update = () => {
    const rect = intro.getBoundingClientRect();
    const viewport = window.innerHeight || 1;
    const progress = Math.min(1, Math.max(0, (viewport - rect.top) / (viewport + rect.height * 0.25)));
    const activeCount = Math.floor(progress * spans.length);

    spans.forEach((span, index) => {
      span.classList.toggle("active", index <= activeCount);
    });
  };

  update();
  window.addEventListener("scroll", update, { passive: true });
  window.addEventListener("resize", update);
}

function createTrailImage(container, src, x, y) {
  const item = document.createElement("img");
  item.className = "trail-item";
  item.src = src;
  item.alt = "";
  item.style.left = `${x}px`;
  item.style.top = `${y}px`;

  container.appendChild(item);

  requestAnimationFrame(() => item.classList.add("show"));

  setTimeout(() => {
    item.classList.remove("show");
    setTimeout(() => item.remove(), 460);
  }, 650);
}

function setupHeroTrail() {
  const hero = $("#hero");
  const trail = $("#heroTrail");

  if (!hero || !trail || !heroTrailImages.length || hero.dataset.trailReady === "true") return;

  hero.dataset.trailReady = "true";

  let index = 0;
  let last = 0;

  hero.addEventListener("pointermove", event => {
    const now = performance.now();
    if (now - last < 120) return;
    last = now;

    const rect = hero.getBoundingClientRect();

    createTrailImage(
      trail,
      heroTrailImages[index % heroTrailImages.length],
      event.clientX - rect.left,
      event.clientY - rect.top
    );

    index++;
  }, { passive: true });
}

