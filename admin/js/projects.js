async function loadProjects() {
  const { data, error } = await db.from('projects').select('*, project_media(*)').order('sort_order', { ascending: true }).order('created_at', { ascending: false });
  if (error) return MeflayAdmin.error(error.message);
  projectState.projects = data || [];
  renderProjectsTable();
}

function filteredProjects() {
  const q = document.getElementById('projectSearch')?.value?.toLowerCase() || '';
  const status = document.getElementById('projectStatusFilter')?.value || 'all';
  return projectState.projects.filter(p => {
    const hay = `${p.title || ''} ${p.slug || ''} ${p.industry || ''} ${p.category || ''}`.toLowerCase();
    return hay.includes(q) && (status === 'all' || p.status === status);
  });
}

