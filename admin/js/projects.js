function openProjectModal(id = null) {
  projectState.editing = id ? projectState.projects.find(p => p.id === id) : null;
  const form = document.getElementById('projectForm');
  form.reset();
  document.getElementById('projectModalTitle').textContent = projectState.editing ? 'Edit project' : 'New project';

  const p = projectState.editing;
  if (p) {
    ['title','slug','subtitle','industry','client','category','services','scope','overview','challenge','approach','result','cover_url','status','sort_order'].forEach(name => {
      if (form[name]) form[name].value = p[name] ?? '';
    });
    form.is_featured.checked = !!p.is_featured;
    form.tags.value = (p.tags || []).join(', ');
    form.colors.value = (p.colors || []).join(', ');
    form.gallery_urls.value = (p.project_media || []).sort((a,b)=>(a.sort_order||0)-(b.sort_order||0)).map(m => m.url).join('\n');
  } else {
    form.status.value = 'draft';
    form.sort_order.value = (projectState.projects.length || 0) + 1;
  }

  MeflayAdmin.openModal('projectModal');
}

async function saveProject(event) {
  event.preventDefault();
  const form = event.target;
  const button = form.querySelector('[type="submit"]');
  MeflayAdmin.setLoading(button, true);

  const payload = {
    title: form.title.value.trim(),
    slug: MeflayAdmin.slugify(form.slug.value || form.title.value),
    subtitle: form.subtitle.value.trim(),
    industry: form.industry.value.trim(),
    client: form.client.value.trim() || 'Branding Photography',
    category: form.category.value.trim(),
    services: form.services.value.trim(),
    scope: form.scope.value.trim(),
    overview: form.overview.value.trim(),
    challenge: form.challenge.value.trim(),
    approach: form.approach.value.trim(),
    result: form.result.value.trim(),
    cover_url: form.cover_url.value.trim(),
    status: form.status.value,
    sort_order: Number(form.sort_order.value || 0),
    is_featured: form.is_featured.checked,
    tags: MeflayAdmin.parseList(form.tags.value),
    colors: MeflayAdmin.parseList(form.colors.value),
    updated_by: MeflayAdmin.user.id
  };

  let projectId = projectState.editing?.id;
  let result;
  if (projectId) {
    result = await db.from('projects').update(payload).eq('id', projectId).select().single();
  } else {
    result = await db.from('projects').insert({ ...payload, created_by: MeflayAdmin.user.id }).select().single();
  }
  if (result.error) {
    MeflayAdmin.setLoading(button, false);
    return MeflayAdmin.error(result.error.message);
  }

  projectId = result.data.id;
  const urls = form.gallery_urls.value.split('\n').map(v => v.trim()).filter(Boolean);
  await db.from('project_media').delete().eq('project_id', projectId);
  if (urls.length) {
    const mediaRows = urls.map((url, index) => ({ project_id: projectId, url, media_type: /\.(mp4|mov|webm)(\?|$)/i.test(url) ? 'video' : 'image', sort_order: index + 1, alt: payload.title }));
    const { error: mediaError } = await db.from('project_media').insert(mediaRows);
    if (mediaError) MeflayAdmin.error(mediaError.message);
  }

  await MeflayAdmin.log(projectState.editing ? 'update_project' : 'create_project', 'projects', projectId, { slug: payload.slug });
  MeflayAdmin.toast('Project saved');
  MeflayAdmin.closeModal('projectModal');
  MeflayAdmin.setLoading(button, false);
  await loadProjects();
}

async function toggleHide(id) {
  const project = projectState.projects.find(p => p.id === id);
  const status = project.status === 'hidden' ? 'published' : 'hidden';
  const { error } = await db.from('projects').update({ status, updated_by: MeflayAdmin.user.id }).eq('id', id);
  if (error) return MeflayAdmin.error(error.message);
  MeflayAdmin.toast(status === 'hidden' ? 'Project hidden' : 'Project published');
  await loadProjects();
}

async function moveProject(value) {
  const [id, dir] = value.split(':');
  const project = projectState.projects.find(p => p.id === id);
  if (!project) return;
  const next = Math.max(1, Number(project.sort_order || 0) + (dir === 'up' ? -1 : 1));
  const { error } = await db.from('projects').update({ sort_order: next }).eq('id', id);
  if (error) return MeflayAdmin.error(error.message);
  await loadProjects();
}

async function importCurrentProjects() {
  if (!confirm('Import current projects into Supabase? Existing slugs will be updated.')) return;
  const button = document.getElementById('importProjects');
  MeflayAdmin.setLoading(button, true);

  for (const project of CURRENT_PROJECTS) {
    const { gallery, ...payload } = project;
    const { data, error } = await db.from('projects').upsert(payload, { onConflict: 'slug' }).select().single();
    if (error) {
      MeflayAdmin.error(`${project.title}: ${error.message}`);
      continue;
    }
    await db.from('project_media').delete().eq('project_id', data.id);
    const rows = (gallery || []).map((url, index) => ({ project_id: data.id, url, media_type: /\.(mp4|mov|webm)(\?|$)/i.test(url) ? 'video' : 'image', sort_order: index + 1, alt: project.title }));
    if (rows.length) await db.from('project_media').insert(rows);
  }

  await MeflayAdmin.log('import_projects', 'projects', null, { count: CURRENT_PROJECTS.length });
  MeflayAdmin.toast('Projects imported successfully');
  MeflayAdmin.setLoading(button, false);
  await loadProjects();
}

